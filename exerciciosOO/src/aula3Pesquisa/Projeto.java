package aula3Pesquisa;

public class Projeto {
	private String nome, area;
	private Pesquisador pesq;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public Pesquisador getPesq() {
		return pesq;
	}
	public void setPesq(Pesquisador pesq) {
		this.pesq = pesq;
	}
	
	
}
